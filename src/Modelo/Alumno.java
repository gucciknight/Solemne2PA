package Modelo;

public class Alumno {
    int idAlumno;
    int idnivelAlumno;
    String loginAlumno;
    String claveAlumno;
    String nombreAlumno;
    String apellidosAlumno;

    public int getIdAlumno() {
        return idAlumno;
    }

    public void setIdAlumno(int idAlumno) {
        this.idAlumno = idAlumno;
    }

    public int getIdnivelAlumno() {
        return idnivelAlumno;
    }

    public void setIdnivelAlumno(int idnivelAlumno) {
        this.idnivelAlumno = idnivelAlumno;
    }

    public String getLoginAlumno() {
        return loginAlumno;
    }

    public void setLoginAlumno(String loginAlumno) {
        this.loginAlumno = loginAlumno;
    }

    public String getClaveAlumno() {
        return claveAlumno;
    }

    public void setClaveAlumno(String claveAlumno) {
        this.claveAlumno = claveAlumno;
    }

    public String getNombreAlumno() {
        return nombreAlumno;
    }

    public void setNombreAlumno(String nombreAlumno) {
        this.nombreAlumno = nombreAlumno;
    }

    public String getApellidosAlumno() {
        return apellidosAlumno;
    }

    public void setApellidosAlumno(String apellidosAlumno) {
        this.apellidosAlumno = apellidosAlumno;
    }
    
    
}
